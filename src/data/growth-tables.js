// ── Percentile Calculation ──

function normalCDF(z) {
  const t = 1 / (1 + 0.2316419 * Math.abs(z));
  const poly = t*(0.319381530 + t*(-0.356563782 + t*(1.781477937 + t*(-1.821255978 + t*1.330274429))));
  const p = 1 - (1/Math.sqrt(2*Math.PI)) * Math.exp(-0.5*z*z) * poly;
  return z >= 0 ? p : 1 - p;
}

export function getPercentile(kg, ageMonths, sex) {
  const table = sex === "girl" ? WHO_LMS_GIRLS : WHO_LMS_BOYS;
  const mo = Math.min(Math.round(ageMonths), 24);
  if (mo < 0) return null;
  const row = table[mo];
  if (!row) return null;
  const [, L, M, S] = row;
  let z;
  if (Math.abs(L) < 0.0001) {
    z = Math.log(kg / M) / S;
  } else {
    z = (Math.pow(kg / M, L) - 1) / (L * S);
  }
  z = Math.max(-3.5, Math.min(3.5, z));
  const pct = normalCDF(z) * 100;
  return Math.round(pct * 10) / 10;
}

export function getHeightPercentile(cm, ageMonths, sex) {
  const table = sex === "girl" ? WHO_LENGTH_LMS_GIRLS : WHO_LENGTH_LMS_BOYS;
  const mo = Math.min(Math.round(ageMonths), 24);
  if (mo < 0) return null;
  const row = table[mo];
  if (!row) return null;
  const [, L, M, S] = row;
  let z;
  if (Math.abs(L) < 0.0001) {
    z = Math.log(cm / M) / S;
  } else {
    z = (Math.pow(cm / M, L) - 1) / (L * S);
  }
  z = Math.max(-3.5, Math.min(3.5, z));
  const pct = normalCDF(z) * 100;
  return Math.round(pct * 10) / 10;
}

